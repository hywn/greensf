        <div class="name" id="here1">Local Food Hub</div>
        <div class="bio">
        Charlottesville, VA<br>
        Contacts: Kristen Suokko (Executive Director)<br>
        Founded in 2009, G2G Supplier since 2014<br>
        <a target="_blank" href="http://www.localfoodhub.org">www.localfoodhub.org</a><br><br>

        The Local Food Hub has undoubtedly been one of our most invaluable partners in our mission to connect the UVa community with local produce. The organization was born back in 2009 out of a community-supported discussion that identified a need for greater linkages between small farms and institutions seeking local food: Local farmers were being locked out of markets due to missing infrastructure, delivery minimums, insurance requirements, and time, while businesses and institutions interested in sourcing locally found it challenging to access a consistent supply of produce from small farms. By providing support services, infrastructure, and market opportunities for local farmers, and simplifying the process of buying local for businesses, the Local Food Hub has helped strengthen our local food economy and put local food on more plates in the Charlottesville/Albemarle region. </div>

        <div class="name" id="here2">Shady Lane Family Farm</div>
        <div class="bio">
        Free Union, Virginia, 20 miles north-west of Charlottesville<br>
        Farmers: Luke Yoder & Maynard Swarey<br>
        Founded in 1999, G2G Supplier since 2014<br>
        Favorite Products: Doris’ homemade granola, free-range eggs<br>
        GAP Certified*<br>
        2012 LFH Partner Producer of the Year<br>
        <a target="_blank" href="http://www.shadylanefamilyfarm.com/">www.shadylanefamilyfarm.com</a><br><br>

        For Doris and Nathan Yoder, farming and family go hand in hand - They started Shady Lane Family Farm in 1999 as a way to provide stability and a wholesome upbringing for their ten children. Today, their kitchen table overlooks an orderly four cultivated acres and 5,000 square feet of greenhouse space, which produce a wide variety of vegetables for local markets and wholesale. The Yoders use organic practices - no chemical fertilizers or sprays - and succession planting in order to manage pests and disease, focusing their energy on keeping plants healthy rather than rehabilitating sick ones.
        Every semester Greens to Grounds staff have enjoyed pulling up to the property to find the kids rope-swinging into the pond and the smell of fresh-baked granola wafting from the bakery.  Outside of your Greens to Grounds box, you can find Shady Lane Family Farm produce at both the Market at Foxfield on Thursdays and Charlottesville City Market on Saturdays.</div>

        <div class="name" id="here3">Caromont Farm</div>
        <div class="bio">
        Albermarle County, 23 miles south of Charlottesville<br>
        Farmers/cheesemakers: Daniel Page and Gail Hobbs-Page<br>
        Founded in 2007, G2G Supplier since Spring 2016<br>
        Favorite Products: Esmontonian and fresh Farmstead chevre<br>
        <a target="_blank" href="http://www.caromontcheese.com/">www.caromontcheese.com</a><br><br>

        Caromont Farm is a hyper-local endeavor that has won national acclaim.  Farmers and cheesemakers Daniel Page and Gail Hobbs-Page converted their home and farm into a farmstead goat operation back in 2007 with the goal of crafting quality cheeses with a sense of “place”. Their business centers on the belief that great cheese comes only from great milk.  Their goat cheeses come from goats born and milked on the farm - a herd of Alpines, Sannens, and La Mancha goats lovingly referred to as the “Caromont Gals” - while their cow’s milk cheeses are produced with milk from Nathan Vergins’ herd of grass-fed Jerseys at Silky Cow Farm in nearby North Garden, Virginia.  Gail and Daniel are strongly committed to supporting their fellow local farmers and producers, so much so that their aged cheeses (Red Row and Esmontonian) are even washed in local apple cider and red wine, respectively.<br><br>

        All Caromont cheeses are made on farm and are hand-ladled, tended daily, and seasonally produced.  Their milk comes from animals raised on principles of natural, humane husbandry and grass-based management.  To overload the cuteness factor, Caromont flooded national media last winter when they called for volunteers to help snuggle and bottle-feed their 90 new-born kids!</div>

