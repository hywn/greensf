        <div class="name" id="here6">Woodson's Mill</div>
        <div class="bio">
        Lowesville, Virginia (Nelson County)<br>
        Producer: Will Brockenbrough<br>
        Founded in 1794, G2G Supplier since [semester, year]<br>
        Favorite Products: Stone-ground pancake mix<br>
        <a target="_blank" href="http://www.woodsonsmill.com/">www.woodsonsmill.com</a><br><br>

        Woodson’s Mill is a juxtaposition of history and sustainable innovation - The mill in use today was constructed in 1794 and has been under the care of just a handful of families since then. The Brockenbrough family took the reigns in the 80’s, restoring the mill to operation under the guidance of David Woodson Jr. and miller Steve Roberts.  The water-powered mill harnesses energy from the Piney River to stone-grind non-GMO and Virginia-grown corn, wheat, and buckwheat.  Slow grinding on hand-dressed millstones helps to preserve the oils, moisture content, nutrients, and unique flavor of each grain.  And by sourcing non-GMO, local grains, the Brockenbroughs are investing in non-commodity production among Virginia farms and develop consumer demand for fresh, high quality, and sustainably-produced grains and meals.  Outside of your Greens to Grounds box, you can find their products at a variety of markets and specialty shops around Charlottesville. </div>
